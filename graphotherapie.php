<!-- Section bilan graphomoteur-->
<div id="bilan" class="w3-container w3-white" style="padding:70px 5px; max-width: 1307px; margin: auto;">
  <h2 class="w3-center w3-xxlarge Style300">Qu’est-ce que qu'un bilan graphomoteur ?</h2>
		
  <p class="w3-center w3-large w3-padding">Le bilan graphomoteur est un bilan du potentiel et des difficultés de l’enfant, de l’adolescent et de l’adulte face à l’écriture.</p>
	</div>

<div class="w3-container w3-light-grey" style="padding:5px 20px; max-width: 1307px; margin: auto;">
<div class="w3-container w3-half">
    <p class="w3-left w3-large"><strong>Il comporte :</strong><br>
<span class="Style200">•</span> un entretien approfondi;<br>
<span class="Style200">•</span> un examen de l’écriture<br>(test ADE et test de vitesse GGRE);<br>
<span class="Style200">•</span> une observation de la tenue de l’instrument,<br>des postures et du déroulement du geste graphique.
</p>
	</div>
	<div class="w3-container w3-half">
    <p class="w3-left w3-large"><strong>A l’issue de ce bilan, le graphothérapeute va pouvoir :</strong><br>
<span class="Style200">•</span> identifier la nature du ou des problèmes;<br>
<span class="Style200">•</span> orienter éventuellement le sujet vers un autre spécialiste (médecin, psychologue, neuropsychologue, ergothérapeute, orthophoniste, orthoptiste, psychomotricien, etc.) pour des examens complémentaires;<br>
<span class="Style200">•</span> proposer un plan de rééducation personnalisé.</p>
	</div>
</div>

	
