<div class="w3-container w3-half w3-left w3-large Style401">
<strong>Parents, si l’un des symptômes suivants apparait :</strong><br>
<span class="Style200">•</span> l’écriture de votre enfant est illisible, lente,<br>fatigante, maladroite;<br>
<span class="Style200">•</span> son cahier manque d’ordre et de netteté;<br>
<span class="Style200">•</span> il souffre quand il écrit.<br>
</div>
	
<div class="w3-container w3-half w3-left w3-large Style401">
<strong>Adultes, si vous remarquez l’un des symptômes suivants :</strong><br>
<span class="Style200">•</span> vous éprouvez certaines des difficultés<br>décrites ci-contre;<br>
<span class="Style200">•</span> des douleurs apparaissent quand vous écrivez;<br>
<span class="Style200">•</span> votre écriture manuscrite ne correspond pas<br>à ce que vous en attendez<br>sur le plan personnel et/ou professionnel.</div>
	
