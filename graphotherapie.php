	<div class="w3-container w3-half">
    <p class="w3-left w3-large"><strong>Les signes les plus courants peuvent être :</strong><br>
<span class="Style200">•</span> un ensemble irrégulier, mal présenté et peu soigné, avec des erreurs de formes et de proportions;<br>
<span class="Style200">•</span> une atteinte de la continuité de l’écriture qui présente de nombreux « accidents »
(lettres qui s’entrechoquent, geste saccadé, retouches);<br>
<span class="Style200">•</span> un geste d’écriture fatigant, source de tension, d’efforts excessifs et douloureux
<span class="Style200">•</span> une lenteur extrême.</p>
	</div>
	<div class="w3-container w3-half">
    <p class="w3-left w3-large"><strong>Les causes peuvent être en lien avec :</strong><br>
<span class="Style200">•</span> une tenue inadéquate de l’instrument graphique ou une mauvaise position de l’ensemble du corps;<br>
<span class="Style200">•</span> un trouble auditif, visuel ou moteur;<br>
<span class="Style200">•</span> un manque de maturité au moment de l’apprentissage (schéma corporel mal intégré, latéralité pas encore établie);<br>
<span class="Style200">•</span> un haut potentiel intellectuel;<br>
<span class="Style200">•</span> des troubles associés : trouble de l’attention avec ou sans hyperactivité, dyslexie, dysorthographie, dysphasie, dyspraxie, etc.
</p>
	</div>
