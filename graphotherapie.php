<div id="definition" class="w3-container w3-white" style="padding:50px 5px; max-width: 1307px; margin: auto;">
<div class="w3-container" style="padding:0px 10px" id="presentation">
<h2 class="w3-center w3-xxlarge Style300">Qu’est-ce que la graphothérapie ?</h2>
		
<div class="w3-row w3-padding">
  <p class="w3-left w3-large">La graphothérapie est un soin apporté à une écriture déficiente et insatisfaisante pour son scripteur&nbsp;(enfant, adolescent ou adulte).<br>
<strong><a href="#bilan">Un bilan graphomoteur</a></strong> permettra d’identifier la nature du ou des troubles rencontrés et de mettre en place une graphothérapie adaptée.</p><br>
  <p class="w3-left w3-large">
    <em>« La graphothérapie (ou « rééducation de l’écriture ») s’applique à l’écriture perturbée mais peut aussi, en pédagogie normale, favoriser son aménagement. »</em> Robert Olivaux.</p>
</div>
	
</div>
</div>

<div class="w3-container w3-light-grey" style="padding:0px 5px; max-width: 1307px; margin: auto;">
	<div class="w3-container w3-half">
	<h1 class="w3-xlarge Style300">Ses objectifs</h1>
    <p class="w3-left w3-large">Grâce à une relation d’aide individuelle, les objectifs de la graphothérapie sont de :<br><br>
<span class="Style200">•</span> restaurer les fonctions graphiques perturbées en soutenant la récupération de la lisibilité et de la vitesse (l’écriture redevient ainsi un instrument de communication aisé et efficace dans son utilisation);<br><br>
<span class="Style200">•</span> permettre à l’écriture de se libérer en trouvant son expression personnelle.
</p>
	</div>
	<div class="w3-container w3-half">
	<h1 class=" w3-xlarge Style300">Ses techniques</h1>
    <p class="w3-left w3-large">Selon la méthode de Robert Olivaux, enrichie des travaux du professeur Julian de Ajuriaguerra et d’Hélène de Gobineau, le graphothérapeute s'appuie sur :<br><br>
<span class="Style200">•</span> la relaxation du geste graphique;<br><br>
<span class="Style200">•</span> les exercices de motricité fine;<br><br>
<span class="Style200">•</span> les exercices liés au graphisme.
</p>
	</div>
</div>



