<span class="Style32">Article édité en Juillet 2021</span>
<h3 class="Style5000">Les caractéristiques de l'écriture :<br>#1 Le Trait</h3>
<span class="Style250">L’écriture est composée de 4 éléments interdépendants : le trait, la forme, l’espace et le mouvement. Pour donner naissance à une écriture lisible et satisfaisante, l’élève doit les accorder de façon équilibrée et harmonieuse.
Cet été, parcourons ensemble les caractéristiques de chacun d’entre eux. Aujourd’hui : le trait.</span><br><br>
<span class="Style25">
Le trait se caractérise par son appui (appuyé/léger, sillon/plat, etc.), sa dimension (fin ou large) ou encore sa texture (net, pâteux, moiré). Il est personnel, dépend du scripteur, du choix de l’instrument et de la façon dont il est tenu. C’est d’ailleurs la seule des 4 caractéristiques de l’écriture qui ne s’enseigne pas.

Pour évaluer le trait, il faut faire appel non seulement à la vue, mais également au toucher : nous observons sa trame, sa couleur, sa consistance.

L’observation du trait, matière première de l’écriture, nous permet de comprendre l’attitude du scripteur vis-à-vis de son environnement (sa motivation, sa sensibilité, mais aussi sa relation au monde).
Rendez-vous dans 2 semaines pour la deuxième composante de l’écriture : la forme.</span><br><br>
