<div class="w3-row-padding">
<h2 class="w3-center Style300 w3-xxlarge">Conditions d’évaluation<br>des acquis de la formation</h2>
	
<div class="w3-container w3-half">
    <p class="w3-left">A chaque début de cours, le formateur introduit son cours en le reliant aux cours précédents et s’assure de la bonne intégration de ces derniers par les stagiaires.<br><br>
	Deux examens (une épreuve théorique et une épreuve technique et pratique) sont organisés la première année. Les stagiaires qui n’obtiennent pas la moyenne à ces deux épreuves (soit 30 points sur 60), devront reprendre certains cours de 1ère année, mais sont autorisés à suivre les cours de 2ème année. Ils ne pourront néanmoins être en charge d’un patient qu’à la condition d’avoir comblé les lacunes constatées.<br><br>
	Rédaction et soutenance d’un mémoire :<br>Dès le début de la 2ème année, et si les conditions pour le faire sont remplies, le stagiaire doit prendre une personne en rééducation. Fondé sur ce premier cas de rééducation d’un patient, un mémoire présente le déroulement de sa prise en charge. Il est complété par l’exposé d’une problématique particulière s’articulant autour des questionnements qui ont pu émerger au cours de</p>
</div>
	
<div class="w3-container w3-half">
 <p class="w3-left">la rééducation et ont nécessité des recherches personnelles approfondies. Le mémoire doit être soutenu dans les 18 mois qui suivent la fin de la formation, en janvier ou en juin.<br><br>
Le stagiaire ayant obtenu une note globale supérieure ou égale à 30/60 à son mémoire et à sa soutenance est admis. Il reçoit le diplôme d’Enseignement Supérieur Privé du GGRE et est autorisé à utiliser le titre de  « Graphothérapeute-Rééducateur de l’Ecriture GGRE ». Il peut devenir rééducateur de l’écriture.<br><br>
Le stagiaire ayant obtenu une note inférieure à 30/60 est ajourné ou recalé. Le jury peut proposer de :
reprendre la rédaction de son mémoire en tenant compte des corrections qui lui ont été adressées, et le soumettre à nouveau à une correction, se présenter à une nouvelle soutenance orale,
prendre un nouveau cas de rééducation, rédiger à nouveau un mémoire, et présenter une nouvelle soutenance. Toute autre solution jugée opportune peut être proposée par le jury qui demeure souverain dans ses décisions.
</p>
