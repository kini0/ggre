<span class="Style250">Qualiopi, une marque de certification qualité<br><br></span>
<span class="Style25"> 
A partir du 1er janvier 2022, les prestataires de formation devront obligatoirement être certifiés Qualiopi  (et toujours déclarés à la Dirrecte) pour prétendre à ces financements. La certification Qualiopi se substitue à Datadock. Elle est basée sur le Référentiel National (ou RNQ ).<br><br>
Ce référentiel national unique a pour objectif d’attester et d’harmoniser la qualité de l’offre de formation professionnelle à travers des modalités d’audit communes pour tous les prestataires et de proposer une plus grande lisibilité de l’offre de formation auprès des parties prenantes (bénéficiaires, financeurs, entreprises).<br><br>
Gage de qualité de notre formation à la graphothérapie
Le RNQ comporte 7 critères qualité divisés en 32 indicateurs précisant les process.<br><br>

L’ensemble de ces critères a pour objectif de :<br>
•	Favoriser l’information au public et la transparence dans la mise en œuvre des actions de formation<br>
•	Garantir la qualité et le sérieux de la formation dans l’optique d’une insertion professionnelle réussie<br><br>

En garantissant le respect de ces 7 critères, la certification Qualiopi atteste la qualité et le sérieux de notre formation. Elle est l’issue d’un long processus d’audit qui nous a permis de renforcer le professionnalisme de notre offre.<br><br>
	
<span class="Style250">Les conditions de la certification du GGRE par l’AFNOR</span>
Contrairement à la démarche Datadock, qui reposait sur une auto-déclaration en ligne par l’organisme de formation et sur la production d’un certain nombre de documents, Qualiopi se base sur une démonstration à un organisme certificateur des processus opérationnels à l’appui des documents présentés. Nous avons confié la mission de certification à l’AFNOR.<br><br>

La durée de validité de la certification Qualiopi est de 3 ans. Elle a exceptionnellement été prolongée d’une année en raison de la crise sanitaire pour tous les organismes certifiés avant le 31 décembre 2020. Nous sommes donc certifiés jusqu’au 9 juillet 2024, avec un audit de surveillance intermédiaire et un audit de renouvellement à l’issue de cette période.<br><br>

En plus d’être déclarés comme établissement d’Enseignement Supérieur Privé auprès du rectorat de Paris, nous sommes donc désormais un organisme de formation professionnelle certifié Qualiopi. Une corde de plus à notre arc… et un gage de qualité supplémentaire pour celles et ceux qui décident de se former à la graphothérapie au GGRE.</span><br><br>
<span class="Style1">
1/Qualiopi est une marque déposée à l’INPI. Son utilisation est soumise à l’obtention de la certification délivrée par un organisme certificateur.<br>
2/Le RNQ est défini par le décret n° 2019-565 du 6 juin 2019.<br>
3/Organisme certificateur lui-même autorisé par le COFRAC (Comité français d’accréditation) à engager des activités de certification sur la base du Référentiel National Qualité.</span>

