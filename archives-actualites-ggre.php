<span class="Style32">Article édité en Juillet 2021</span>
<h3 class="Style5000">Graphologie et graphothérapie :<br>quelles différences ?</h3>
<span class="Style250">La graphologie et la graphothérapie ont en commun l’écriture. Si la première discipline s’attache à interpréter le lien entre l’écriture et la personnalité de son auteur, la seconde vise à rééduquer le geste graphique.</span><br><br>
<img src="images/GGRE-photo-graphologue.jpg" width="100%"/><br><br>
<strong>1. A l’origine des deux disciplines : l’écriture</strong><br><br>
<span class="Style25">
Au même titre que l’iris de l’œil, les empreintes digitales ou les oreilles, l’écriture manuscrite est propre à chacun d’entre nous et permet de nous différencier des autres.<br><br>

S’il s’agit bien entendu d’un véritable outil de communication, l’écriture est unique en ce sens qu’elle reflète l’identité de chaque individu : nous structurons et transmettons notre pensée par l’écrit comme une représentation de nous-mêmes. Depuis l’origine de l’humanité, l’écriture permet ainsi de laisser une trace : les hommes préhistoriques gravaient déjà des symboles dans la roche… Ecrire est le propre de l’homme et résulte du besoin inné de laisser des traces, de communiquer, de s’exprimer.<br><br></span>

