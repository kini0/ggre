Pour les enfants de CP, l’apprentissage de la lecture, entamé quelques mois plus tôt, s’est poursuivi à la maison avec l’aide des parents… mais celui de l’écriture exige d’autres compétences : il faut savoir tenir correctement son crayon, bien placer la main, réaliser les lettres dans le bon sens, apprendre à les lier entre elles… Apprendre à écrire est long et difficile et requiert de l’attention, de la précision, des démonstrations, de l’entraînement, de la régularité et de l’autonomie. Stoppés dans leur apprentissage de l’écriture, certains enfants se sont trouvés en décalage avec les exercices habituellement demandés à cette période de l’année. Les quelques jours de retour à l’école au mois de juin ne leur ont pas suffi pour rattraper leurs éventuels retards.<br><br>
Après les vacances d’été, ces mêmes enfants sont entrés en CE1 avec des niveaux extrêmement disparates. Certains d’entre eux avaient progressé sur leur lancée tandis que d’autres avaient accumulé presque six mois de retard, et parfois même régressé. Ce retard est particulièrement sensible en ce qui concerne l’écriture. Pourtant, les programmes scolaires n’ont pas été allégés en CE1 et ils ne tiennent aucunement compte des lacunes de certains élèves liées à l’absence d’école. Là encore, les enseignants font ce qu’ils peuvent pour tenter d’assurer le programme officiel sans laisser d’enfants sur le côté.<br><br>
Ecrire est un acte complexe. Les enfants de CE1 dont l’écriture n’aura pas acquis le niveau nécessaire risquent d’être pénalisés dans les années qui viennent où on leur demandera plus d’aisance et de rapidité. Il est important de les soutenir et de les accompagner pour leur éviter un décalage trop important au regard des attentes scolaires, car les mois confinés risquent de peser plus lourd que prévu sur leur scolarité.</span><br><br>
