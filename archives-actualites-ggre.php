<h3 class="Style5000">Le GGRE obtient la certification Qualiopi pour ses actions de formation à la graphothérapie</h3>
<span class="Style250">Nous sommes fiers de vous annoncer qu’au terme d’une démarche engagée depuis novembre 2019, le GGRE a obtenu la certification Qualiopi le 10 juillet 2020 pour ses actions de formation à la graphothérapie.<br><br></span>
<span class="Style25">
Outre la possibilité pour nos élèves de continuer à obtenir des financements pour suivre notre formation, cette certification est un gage de qualité dans l'exercice futur de leur activité de graphothérapeute.<br><br>

<span class="Style250">Le GGRE, un organisme de formation référencé au Datadock depuis 2017</span><br>
Avant janvier 2017, seule une déclaration d’activité auprès de la Direction Régionale des Entreprises de la Concurrence, de la Consommation, du Travail et de l’Emploi (Direccte) était nécessaire pour exercer une activité de formation et prétendre à un financement de la part des organismes financeurs.

Afin de renforcer la qualité des organismes de formation, le Datadock (base de données sur la formation professionnelle) a depuis été mis en place. Ainsi, depuis janvier 2017, seuls les organismes déclarés à la Direccte et référencés au sein de Datadock pouvaient permettre à leurs apprenants de bénéficier des financements publics. Le GGRE avait obtenu en juillet 2017 son référencement au sein de Datadock.</span><br><br>
