<span class="Style25"><strong>Que ressent un enfant lorsqu’il est la cible de toutes ces foudres ?</strong><br><br>

De l’injustice ? De la colère ? De l’humiliation ? De la tristesse ? Une petite égratignure (ou une blessure plus profonde) dans sa confiance en lui ? Tout à la fois peut-être. En plus de réduire l’enfant à sa production écrite, ces mots délétères et dégradants influencent l’image que l’enfant a de lui-même. L’enfant qui présente des problèmes pour écrire essaie pourtant certainement de bien faire, en tirant la langue parce que c’est pour lui un gros effort. Mais il a beau s’appliquer à bien former les lettres comme on le lui a enseigné, celles-ci finissent toutes cabossées, comme si on les avait cognées. S’il s’évertue à écrire sur les lignes, à ne pas écrire trop grand et à ne pas « dépasser », les lettres semblent ne pas vouloir rester dans les interlignes et les mots dansent sur la page. Et quand il doit souligner, sa maudite règle bouge tout le temps ! Conscient du manque de propreté de sa page, l’enfant souffre intérieurement de ne pas réussir à produire ce qu’on attend de lui. Lui-même serait tellement fier d’y parvenir. L’accabler ne fait qu’augmenter sa peine.<br><br>

<strong>L’écriture : un long apprentissage</strong><br><br>

Il faut penser à tout, en même temps. Comme lorsque l’on apprend à conduire. Les premiers mètres sont difficiles et angoissants tant les vérifications à opérer avant de démarrer sont nombreuses et aussi importantes les unes que les autres. Si on en oublie une, on risque l’accident ! Apprendre à écrire est un travail long et difficile, qui s’étire sur plusieurs années et requiert de la part des enfants une bonne dose d’attention, de concentration, d’effort et d’énergie. Le tout sur une base encore mouvante car leur développement psychomoteur n’est pas toujours arrivé à un niveau suffisant pour répondre à cette demande particulière qu’est l’écriture.
Il faut savoir se diriger dans l’espace, dans le temps, être bien coordonné, connaitre son schéma corporel… Autant de prérequis auxquels on ne pense pas, mais qui sont indispensables pour entrer dans l’apprentissage de l’écriture. Bien sûr aussi, les doigts doivent avoir acquis l’agilité nécessaire pour pouvoir accomplir des gestes fins et précis. Reconnaissons que les filles ont souvent ici un petit avantage sur les garçons…<br><br>

<strong>Comment s’adresser à un enfant qui a une écriture illisible ?</strong><br><br>

Oui, c’est vrai, certaines pages ne sont pas bien présentées. On peut même dire qu’elles sont « sales ». Et, en effet, il faut le faire remarquer à l’enfant. Mais comment le lui dire avec des mots plus pédagogiques et moins humiliants que « tu as une écriture de cochon » ?
Notons d’ailleurs que cette phrase n’a aucun sens, aucun cochon n’ayant jamais écrit. De surcroit, contrairement aux idées reçues, le cochon est en réalité très intelligent, propre et sociable, ce qui en fait un animal plein de qualités !
Revenons donc à nos moutons (il y a décidément beaucoup d’animaux dans cet article) : peut-on dire à un enfant que sa page est « sale » ?
Sans parler de saleté, on peut lui montrer que les noircissements et les ratures masquent le contenu de la page. Si nous savons qu’il s’est donné la peine de raturer pour améliorer sa production, le résultat ne reflète pas son intention de départ.
On peut aussi lui faire remarquer que son écriture n’est pas suffisamment claire pour que les autres puissent la lire. L’écriture servant à communiquer, cela est fâcheux. En réalité, ce qui doit être recherché n’est pas le côté esthétique du tracé, mais son efficacité.
Qu’est-ce qu’une écriture efficace ? C’est celle qui est suffisamment lisible par tous, suffisamment rapide pour suivre le rythme demandé et qui ne demande pas trop d’énergie à produire. Voilà l’écriture qui sera satisfaisante pour celui ou celle qui a écrit. Remarquons au passage qu’il n’y a là aucun critère de beauté du graphisme. A contrario, une écriture inefficace, difficile à lire, trop lente pour suivre le rythme de la classe ou qui provoque des douleurs dans le bras, le poignet ou les doigts, pénalise son auteur.<br><br>

<strong>Comment y remédier ?</strong><br><br>

La graphothérapie est une solution aux difficultés d’écriture. Elle permet de travailler le geste graphique en douceur et dans un cadre sécurisant et, entre autres, de récupérer ou de mettre en place les gestes et les réflexes de posture et de tenue du stylo les plus adaptés pour écrire. Un accompagnement qui aide l’enfant à porter un regard positif sur son écriture… et sur lui-même.<br><br>
	
