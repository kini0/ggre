Le gribouillis de l’enfant est la première trace que l’être humain dépose sur le papier. Lorsque nous apprenons à écrire, nous appliquons le modèle calligraphique auquel nous allons progressivement apporter des modifications spontanées en fonction de notre personnalité.
<br><br>
<img src="images/GGRE-modele-calligraphique.jpg" width="100%"/><br><br>
Si l’écriture se stabilise au terme de l’apprentissage scolaire, elle va continuer d’évoluer tout au long de notre vie. Selon les humeurs, les doutes et les épreuves personnelles rencontrées, chacun apporte ses propres modifications au modèle enseigné. Ce sont ces modifications qui sont analysées en graphologie.</span><br><br>
<strong>2. La graphologie : l’étude de la personnalité</strong><br><br>
<span class="Style25">
La graphologie est une science humaine qui consiste à étudier la personnalité de l’auteur d’un texte. En effet, comme le peintre met sa patte personnelle dans ses tableaux, l'auteur met sa patte dans ses écrits : son écriture est parsemée d’indices sur sa personnalité.<br><br>
Lors d’une séance de travail, le graphologue observe l’écriture sans s’attacher au sens des mots, que parfois même, il ne lit même pas. Il va d’abord décrire le contexte général de l’écriture, puis ses spécificités (taille des lettres, espace entre les lettres et les lignes, inclinaison de l’écriture, etc.). Ces dernières seront toujours interprétées en fonction du contexte général pour comprendre la personnalité de l’auteur. Les analyses graphologiques peuvent être utiles dans le cadre d’un recrutement professionnel, d’une recherche d’orientation ou même d’un travail sur soi…
</span><br><br>
<strong>3. La graphothérapie : la rééducation de l’écriture</strong><br><br>
<span class="Style25">
La graphothérapie, également appelée rééducation de l’écriture, est destinée aux enfants, adolescents et adultes qui sont en difficulté avec leur écriture. Ces difficultés peuvent se traduire par des problèmes de lisibilité, de lenteur ou de douleurs à la main ou au bras.
<br><br>
La mission du graphothérapeute n’est pas de modifier radicalement leur écriture pour la conformer à un modèle idéal, mais de les aider à produire une écriture satisfaisante pour eux-mêmes et pour ceux qui les lisent : une écriture lisible, aisée, suffisamment rapide et qui leur correspond afin qu’ils en soient fiers et heureux. La graphothérapie est bien une thérapie de l'écriture, et non une thérapie par l’écriture, bien qu'une amélioration de cette dernière puisse participer au bien-être de l’individu.
<br><br>
La discipline s’appuie sur les travaux du Professeur Julian de Ajuriaguerra (1911-1993), neuropsychiatre et psychanalyste, qui s'est intéressé au développement de l'enfant et aux troubles de l'écriture dès les années 1960. Ses travaux ont été repris et enrichis par Robert Olivaux, psychologue et graphologue, qui a développé une méthode basée sur une écoute individuelle et la détente du geste graphique. Il s’agit de libérer le geste afin d’écrire avec souplesse et fluidité. Plus récemment, le groupe de recherche EnnéADE mené par Adeline Gavazzi-Eloy a développé l’échelle de vigilance ADE, un outil d’évaluation inspiré des travaux sur l’approche dynamique de l’écriture.<br><br>
	
<span class="Style250">
Pour Caroline Baguenault, présidente du Groupement des Graphothérapeutes-Rééducateurs de l’Écriture, « de solides connaissances de l’observation de l’écriture permettent de comprendre comment les différents facteurs de l’écriture (trait, forme, espace et mouvement) interagissent entre eux. Les outils dont nous disposons en tant que graphothérapeutes nous permettent d’analyser les dysfonctionnements de l’écriture et d’y apporter des solutions adaptées. »</span><br><br>
	
La pratique des membres du GGRE est basée sur cette méthode personnalisée et bienveillante impliquant trois acteurs à parts égales : le scripteur, son écriture et le graphothérapeute.</span><br><br>
<img src="images/GGRE-graphotherapie-enfant.jpg" width="100%"/><br><br>
<strong>4.	La graphologie et la graphothérapie au cœur du GGRE</strong><br><br>
<span class="Style25">
Le GGRE se distingue des autres groupements de spécialistes grâce à une mise en commun des deux disciplines : graphologie et graphothérapie.<br><br>

L’ensemble de nos membres et des professionnels formés au sein de <strong><a href="formation-graphotherapie.php">notre organisme,</a></strong> dispose d’une connaissance approfondie de l'observation de l'écriture, de ses facteurs d’évolution et de l’origine des difficultés. Certains d’entre nous sont par ailleurs graphologues.
<br><br>
Nos connaissances en graphologie sont précieuses. En pratiquant une observation fine de l'écriture, nous sommes en mesure de considérer l’écriture dans son ensemble, d’identifier les points forts et les difficultés des patients, et de les accompagner tout en respectant leur spécificité et leur sensibilité.

</span><br><br>
