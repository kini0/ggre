<span class="Style32">Article édité en Juin 2021</span>
<h3 class="Style5000">Ecriture illisible :<br>mon enfant écrit comme un cochon, que faire ?</h3>
<span class="Style250">Ecriture illisible, feuilles brouillonnes ou manque de soin… Que ressentent les enfants à qui l’on reproche sans cesse de « mal » écrire et comment réagir en tant que parent pour les aider à s’améliorer ?</span><br><br>
<span class="Style25">Feuilles raturées par le professeur, pages arrachées, punitions, privation de récréation pour recommencer un travail… sans oublier le tampon « cochon » apposé sur des feuilles qui manquent de soin. Les idées ne manquent pas pour souligner une page qui n’a pas satisfait aux consignes de présentation demandées par les enseignants.<br><br>

L’enfant le fait-il intentionnellement ? Certainement pas ! Pas plus qu’il ne manque d’intérêt pour l’école, de respect pour l’enseignant ou qu’il ne fait preuve de « je-m’en-foutisme ». Aucun enfant ne fait exprès d’avoir <strong>une écriture illisible,</strong> d’écrire « mal » ou de livrer une page sale. AUCUN !<br><br></span><img src="images/GGRE-dessin-cochon-enfant.jpg" width="100%" /><br><br>
