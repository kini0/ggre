<span class="Style5000">Trois conseils pour les enfants en âge primaire</span><br><br>

<strong>1. Un diamètre large</strong><br><span class="Style25">
Les petits doigts des enfants peuvent glisser sur des crayons trop fins et à la surface lisse. Pendant les premières années d’école primaire, équipez les enfants de <strong>stylo et crayons de gros diamètre</strong> afin de favoriser la prise en main et le bon positionnement des doigts sur le crayon.<br><br></span>

<strong>2. Une forme ergonomique</strong><br><span class="Style25">
Les <strong>stylos et crayons ergonomiques</strong> favorisent la bonne position des doigts sur l’outil scripteur et permettent d'adopter une posture optimale et détendue pour écrire. Certains sont conçus avec des encoches, des empreintes préformées ou des picots de préhension, qui permettent de guider le positionnement des doigts. 
Les matières antidérapantes aident l’enfant à tenir son stylo sans crainte qu’il ne lui tombe des mains. 
<strong>Les crayons triangulaires</strong> favorisent une bonne prise en main. Privilégiez les mines plutôt tendres (2B, voire HB) pour éviter un appui trop fort sur le papier. 
Par ailleurs, de nombreux outils d’écriture (stylos et crayons variés) se déclinent aujourd’hui en version pour <strong>gaucher ou pour droitier.</strong> Ils constituent une aide appréciable pour les jeunes enfants en phase d’apprentissage, et plus généralement en âge primaire.<br><br></span>

<strong>3. Un lignage adapté</strong><br><span class="Style25">
Les <strong>cahiers</strong> et les <strong>feuilles</strong> vous seront généralement demandés par les enseignants avec des consignes précises (type de ligne ou de carreaux, couleur des couvertures...). Chez les enfants en phase d’apprentissage, le lignage à 4 lignes aide les enfants à se positionner dans l’espace et à acquérir les justes proportions des lettres. Même s’il ne les utilise plus à l’école, n’hésitez pas proposer à votre enfant ce type de lignage pour les devoirs à la maison.<br>Vous n’aurez pas le même choix dans la sélection du cahier de texte ou de l’agenda. Si les lignes de ces derniers sont très serrées, expliquez à votre enfant qu’il peut écrire une ligne sur deux.  
Enfin, laissez votre enfant exprimer ses ressentis vis-à-vis du grain du papier, de la couleur des lignes, etc.</span><br><br>
	
<strong>Les crayons et feutres pour les dessins et coloriages</strong><br><span class="Style25">
Les crayons de couleur un peu gros, triangulaires, si possible avec des encoches ou des picots de préhension, aident l’enfant à tenir correctement son outil. Choisissez-les gras de préférence, toujours pour laisser une trace sans appuyer.<br>
Du côté des feutres, vous trouverez des versions améliorées avec des pointes qui ne s’enfoncent pas, des feutres qui ne sèchent pas même si le capuchon est mal refermé ou encore une encre qui sèche rapidement sans traverser le papier.
</span><br><br>
	
<strong>Quelques astuces pour une trousse bien équipée </strong><br><span class="Style25">
1. Optez pour une gomme blanche standard pour un gommage propre.<br>
2. Préférez une colle colorée en tube qui permet à l’enfant de voir facilement où elle a été appliquée.<br>
3. Les règles et le matériel de géométrie ne sont pas toujours faciles à manipuler pour les enfants. Lorsqu’ils sont fabriqués dans un matériau antidérapant, ils sont plus maniables et plus confortables à utiliser. Choisissez éventuellement des modèles équipés de poignées de préhension si vous le jugez utile. Quant aux compas, certains sont dotés d’un système de blocage des branches qui évite les glissades.<br>
4. Les ciseaux doivent être adaptés à la main de l’enfant. Plus les lames sont courtes, plus l’outil est facile à manipuler. Selon la prise de votre enfant, choisissez des ciseaux avec deux anneaux identiques (le pouce est placé dans l’anneau du haut et l’index dans l’anneau du bas) ou avec des anneaux de grandeurs différentes (le pouce est placé dans l’anneau du haut et l’index et le majeur sont placés dans l’anneau du bas) pour les très jeunes. Enfin, n’oubliez pas les versions adaptées aux petits gauchers.<br>
5. N’oubliez pas de mettre le nom de votre enfant sur les stylos et outils afin d’éviter qu’il ne les perde.<br>
6. N’oubliez pas également de regarder ou de tester régulièrement le stylo-plume de votre enfant s’il en a un. Il n’est pas rare que les jeunes enfants fassent tomber leur stylo. La plume peut alors se tordre si elle rencontre le sol à la verticale, ce qui contrariera la bonne coulée de l’encre. Problème de soin en vue sur les cahiers...</span><br><br>
	
<strong>Les fournitures scolaires pour les gauchers</strong><br><span class="Style25">
Il existe désormais une gamme importante de fournitures scolaires spécialement adaptées pour les gauchers : cahier à spirales inversées, ciseaux, crayons, stylos, taille-crayon avec lames inversées, etc.<br><br>Grâce à un matériel adapté, votre enfant évitera la crispation et la fatigue et pourra se concentrer sur les apprentissages. A lui les joies de l’écriture... et des apprentissages !
</span><br><br>
	
