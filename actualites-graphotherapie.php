<strong>Ecrire pour se représenter sur la feuille</strong><br><span class="Style25">
Chaque écriture est unique, parce qu’elle est le reflet de la personnalité de celui (ou de celle) qui la produit, de ce qu’il (ou elle) est et de ses aspirations. Ecrire à la main, c’est en effet se projeter soi-même sur la feuille, se donner à voir au regard de l’autre, laisser une trace de soi. C’est la raison pour laquelle il est important de ne pas jeter le discrédit sur une <a href="#ecritureillisible">écriture d’enfant</a> ou d’adolescent, au risque que celui-ci se sente atteint personnellement par les critiques sur ses productions écrites 
</span><br><br>
	
<strong>Ecrire pour s’inscrire dans une culture collective</strong><br><span class="Style25">
L’écriture permet enfin de partager et de participer à la culture de l’écrit, source de savoir, d’art et d’émotion en respectant les codes transmis par les générations passées, en s’insérant dans une culture et en devenant soi-même auteur. Quel beau programme !
<br><br>Bien sûr, pour que l’écriture manuscrite remplisse ces différentes fonctions, elle doit être lisible et suffisamment rapide, la vitesse d’écriture s’évaluant selon de l’âge de celui ou de celle qui écrit. Cela impose un geste habile et économe en énergie, une gestion satisfaisante de l’espace, de l’autonomie…<br><br>Le premier pas vers l’autonomie se fait dès que le scripteur s’est détaché du dessin des lettres et a mis en place des automatismes. Autant de difficultés potentielles que peuvent rencontrer les enfants au cours de leur apprentissage de l’écriture cursive. En tant que spécialistes de l’écriture, nous, graphothérapeutes, portons une attention particulière à ces éventuels obstacles. Mais ceci est une autre histoire…
