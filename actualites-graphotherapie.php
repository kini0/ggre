<h3 class="Style5000">A l’heure du numérique,<br>pourquoi écrire à la main reste primordial ?</h3>

<span class="Style250">L’écriture est née il y a 6000 ans en Mésopotamie. C’est son apparition qui définit la fin de la Préhistoire et l’entrée de l’humanité dans l’Histoire, car elle permet de conserver la trace des événements et fait entrer les peuples dans le temps. Aujourd’hui et depuis que l’enseignement est devenu obligatoire en 1882, les enfants apprennent à écrire à la main vers l’âge de six ans, au moment de leur entrée à l’école primaire. Une compétence extraordinaire qui, en dépit de l’avènement du numérique et des claviers, reste primordiale. Pourquoi ? Parce qu’elle a de nombreuses fonctions dont nous n’avons souvent pas conscience…</span><br><br>
	
<span class="Style25">
Le dictionnaire Larousse définit l’écriture comme une « représentation de la parole et de la pensée par des signes graphiques conventionnels. » Il la décrit également comme un « système de signes graphiques servant à noter un message oral afin de pouvoir le conserver et/ou le transmettre. » 

Que cache cette définition rigoureuse et quelles sont les différentes fonctions de l’écriture manuscrite ?

</span><br><br>
	
<strong>Ecrire pour communiquer</strong><br><span class="Style25">
C’est sa fonction la plus évidente : écrire nous permet de communiquer avec les autres en envoyant une lettre ou une carte postale (ce qui, depuis que nous avons des smartphones en poche, est de plus en plus rare) ou communiquer avec soi en prenant des notes ou en écrivant un brouillon. Sur écran, mais d’autant plus sur papier, l’écriture est donc un outil que nous utilisons pour transmettre un message de manière lisible et avec une mise en page conforme et adaptée.
</span><br><br>
	
<strong>Ecrire pour retranscrire sa pensée et s’exprimer</strong><br><span class="Style25">
Mais écrire, c’est aussi retranscrire sa pensée et s’exprimer. C’est la fonction « instrumentale » de l’écriture, outil de langage et d’expression. L’écriture rend lisible la pensée. Plusieurs études récentes montrent également pourquoi écrire à la main est primordial : en plus de nous permettre de produire un message porteur de sens, <a href="archives-actualites-ggre.php#importance_apprendre_ecriture" target="_blank">l’écriture manuscrite</a> nous aiderait à mieux construire et structurer notre pensée. 
</span><br><br>
