<div id="choisir-fournitures-scolaires-enfant" class="w3-col w3-left w3-margin w3-border w3-round w3-padding w3-card-2" style="background-color: white" title="Comment choisir les fournitures scolaires de votre enfant ? - GGRE" alt="Comment choisir les fournitures scolaires de votre enfant ? Voici quelques conseils pour vous aider à choisir les outils adaptés de la trousse de la rentrée.">
<span class="Style32">Article édité en juillet 2022</span>
<h3 class="Style5000">Comment choisir<br>les fournitures scolaires de votre enfant ?</h3>
	
<img src="images/actus-GGRE-choisir-fournitures-scolaires.jpg" width="100%"/><br><br>

<span class="Style250">Un bon ouvrier doit avoir de bons outils...<br>et encore davantage lorsqu’il est en phase d’apprentissage.  
Depuis 2018, le Ministère de l’Education nationale publie une liste des fournitures scolaires essentielles pour les élèves. Néanmoins face à l’étendue de l’offre commerciale, il est parfois difficile de faire un choix éclairé. Afin de vous accompagner dans ce choix, le GGRE vous conseille pour choisir les outils les plus adaptés à l’écriture et à glisser dans la trousse de la rentrée.
</span><br><br>
	
<strong>Les critères pour choisir les bons stylos, crayons et feutres</strong><br><span class="Style25">
Les critères qui doivent vous guider, qu’il s’agisse de stylo-plume, stylo à bille, crayon à papier, crayons de couleur ou feutres, concernent :<br>
•	la prise en main,<br>
•	le confort d'écriture,<br>
•	la fluidité avec laquelle le stylo glisse sur le papier.

</span><br><br>
	
