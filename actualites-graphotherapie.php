<div id="aux-origines-de-la-graphotherapie" class="w3-col w3-left w3-margin w3-border w3-round w3-padding w3-card-2" style="background-color: white" title="Aux origines de la graphothérapie : Ajuriaguerra, Olivaux, Gavazzi-Eloy – GGRE" alt="Les origines de la graphothérapie s'appuient sur les travaux de Julian de Ajuriaguerra, Robert Olivaux, Adeline Gavazzi-Eloy. Découvrez l’histoire de cette discipline.">
<span class="Style32">Article édité en Mai 2022</span>
<h3 class="Style5000">Aux origines de la graphothérapie...</h3>
	
<img src="images/actus-GGRE-origines-graphotherapie.jpg" width="100%"/><br><br>

<span class="Style250">On reste souvent admiratif devant les belles lettres et cartes postales de nos ancêtres. Les difficultés d’écriture seraient-elles apparues avec l’avènement des écrans et des claviers ? Pas vraiment… <strong>Les origines de la graphothérapie</strong> s'appuient sur les travaux et l'expérience de plusieurs médecins et enseignants parmi lesquels on peut citer Julian de Ajuriaguerra, Robert Olivaux et Adeline Gavazzi-Eloy. A partir des années 1950, ceux-ci ont développé des techniques pour aider ceux qui rencontrent des difficultés d’écriture à trouver ou à retrouver une écriture fluide, aisée, lisible, soignée et suffisamment rapide. D’où vient cette discipline et qui sont donc les "grands noms" qui lui ont donné naissance ?</span><br><br>
	
<strong>A l’origine : Julian de Ajuriaguerra</strong><br><span class="Style25">
Neuropsychiatre et psychanalyste français d’origine espagnole, <strong>Julian de Ajuriaguerra</strong> s’installe en France dans les années 1930. En s’intéressant au développement de l'enfant, cet enseignant et chercheur insatiable devient le précurseur de la psychomotricité française. Ses travaux le conduisent à étudier les désordres psychomoteurs et les troubles de l’écriture, mais aussi la dyslexie. Dans les années 1960, il participe à la mise en place d’une consultation hospitalière de graphothérapie destinée aux enfants et aux adolescents à Paris. Il crée également la première échelle d’évaluation de la dysgraphie. Ses ouvrages sur l’écriture de l’enfant et la rééducation de l’écriture sont toujours des références pour les graphothérapeutes.
</span><br><br>
	
