<strong>Le père de la graphothérapie française : Robert Olivaux</strong><br><span class="Style25">
Les travaux d’Ajuriaguerra seront repris et approfondis par le graphologue, docteur en psychologie et psychanalyste <strong>Robert Olivaux.</strong> Passionné par l’écriture, celui-ci développe une méthode de rééducation, qui s’appuie sur de solides bases en graphologie dans une approche pédagogique, humaniste et respectueuse de chacun. Il considère que <a href="actualites-graphotherapie.php#metier-graphotherapeute" target="_blank">le rôle du graphothérapeute</a> est d’amener l’enfant à « être bien dans son écriture » comme on peut l’être « dans sa peau ».
Il fonde la première Association de graphothérapeutes diplômés de la Société Française de Graphologie en 1966. Celle-ci évoluera pour former <a href="association-de-graphotherapeutes.php" target="_blank">le GGRE</a>(Groupement des Graphothérapeutes Rééducateurs de l’Ecriture), dont il est membre d’honneur. Car Robert Olivaux a récemment fêté… ses cent ans !</span><br><br>
	
<strong>Les travaux plus récents relatifs à la graphothérapie</strong><br><span class="Style25">
La graphothérapie est une discipline qui évolue, se renouvelle, s’enrichit de nouvelles connaissances et approches. Elle s’adapte aux scripteurs et s’intéresse aux découvertes scientifiques les plus récentes, comme celles concernant les neurones miroirs ou les réflexes archaïques.<br><br>
De nombreux professionnels ont apporté leur pierre à l’édifice de notre spécialité. Nous ne pouvons achever ce bref tour de table sans parler de la contribution <strong>d’Adeline Gavazzi-Eloy,</strong> enseignante et graphothérapeute, et de son groupe de travail qui ont développé une approche originale : <strong>l’Approche Dynamique de l’Écriture,</strong> accompagnée d’un outil d’évaluation et de diagnostic des difficultés de l’écriture (l’échelle ADE). <strong>Le GGRE</strong> mène également depuis plusieurs années des travaux sur l’évaluation de la vitesse de l’écriture chez les enfants et les adolescents. Mais ceci est une autre histoire dont nous reparlerons prochainement…</span><br><br>
	
