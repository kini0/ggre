<strong>1/ Le rôle du graphothérapeute</strong><br>
<span class="Style25">Rééduquer les troubles de l’écriture<br>
Le graphothérapeute est un professionnel de la rééducation des troubles de l'écriture et de la prise en charge de la dysgraphie.<br><br>

Il accompagne les personnes souffrant d’une écriture en difficulté, voire dysgraphique, c’est-à-dire une écriture qui ne donne pas satisfaction, ce qui peut se manifester de différentes façons :<br>
●	L’écriture est difficilement lisible, voire illisible.<br>
●	Elle n’est pas suffisamment rapide.<br>
●	Elle manque de soin.<br>
●	Ecrire peut être à l’origine de douleurs dans la main, le poignet ou le bras.<br><br>
 

(Re)trouver une écriture efficace<br>
Le rôle du métier de graphothérapeute est d’identifier ces difficultés et d’effectuer un bilan du potentiel et des difficultés de l’enfant, de l’adolescent et de l’adulte face à l’écriture, puis d’apporter un suivi personnalisé au moyen de techniques et d’exercices qui aideront le patient à trouver ou retrouver une écriture efficace. Une écriture efficace est une écriture lisible et suffisamment rapide pour l’âge et le niveau de celle ou de celui qui écrit. Elle ne doit être ni douloureuse, ni fatigante.<br><br>

Un accompagnement adapté à chaque patient<br>
Des difficultés d’écriture peuvent apparaître tout au long de la vie. Le graphothérapeute peut ainsi être amené à intervenir auprès de personnes de tous âges : enfants, adolescents ou adultes. Il adapte la rééducation à ses patients et la mène dans le respect de leur personnalité. Une thérapie dont les bénéfices peuvent dépasser le cadre strict de l’écriture L’écriture est une compétence transversale, indispensable dans le système scolaire. C’est par l’écrit que l’élève est majoritairement évalué tout au long de sa scolarité. Elle lui permet :<br>
●	de communiquer,<br>
●	d’exprimer sa pensée,<br>
●	de laisser une trace de lui-même,<br>
●	de participer à une culture collective, donc de s'intégrer au groupe social.<br><br>
Il n’est pas rare que les bénéfices de la rééducation dépassent le strict cadre strict de l’écriture et aident l’enfant à améliorer son estime de soi. 
<br><br></span>

<strong>2/ Le déroulement d’une séance de graphothérapie</strong><br>
	
<span class="Style25">Après avoir établi le bilan graphomoteur, le graphothérapeute adapte sa prise en charge au patient. Au cours des séances, il s’appuie sur différents outils et techniques, issus notamment des travaux du Docteur Julian Ajuriaguerra (neuropsychiatre et psychanalyste), de Robert Olivaux (graphologue et graphothérapeute) et d’Adeline Eloy (enseignante et graphothérapeute).<br><br> 

Il peut intervenir de manière indépendante à la demande du patient ou s’inscrire dans un accompagnement global. Il opère dans ce cas de façon conjointe avec d’autres professionnels (médecin, psychologue, orthophoniste, enseignant, etc.) et en collaboration avec les parents lorsqu’il s’agit d’un enfant ou d’un adolescent. Une séance de graphothérapie dure en moyenne 45 à 50 minutes à raison d’une fois par semaine. La prise en charge s’étend généralement sur plusieurs mois selon les difficultés du patient et sa progression dans le parcours de rééducation.</span>
<br><br>

<strong>3/ Les formations à la graphothérapie</strong><br>
<span class="Style25">
Choisir une formation sérieuse aux différentes techniques et approches<br>
La formation à la graphothérapie s’adresse le plus souvent à des personnes en reconversion professionnelle disposant ou non d’un bagage dans ce domaine.
Le métier de graphothérapeute est non conventionnel, c’est-à-dire que le diplôme n’est actuellement pas reconnu par l’Etat. Pour exercer de façon professionnelle et pouvoir faire preuve de compétences éprouvées, il est important d’être rigoureux, de choisir une formation solide aux différentes techniques et approches et de toujours intervenir dans le respect du patient. La formation du GGRE, certifiée Qualiopi et déclarée au rectorat de Paris
Le GGRE propose <a href="objectif-et-organisation.php">une formation de deux ans</a> comprenant :<br>
●	Un stage de suivi d’une rééducation en milieu professionnel<br>
●	La rédaction d’un mémoire<br><br>

Notre diplôme est un diplôme d'enseignement supérieur privé déclaré au rectorat de Paris et certifié <a href="https://qualiopi.certif-icpf.org/obtenir-qualiopi" target="_blank">Qualiopi</a>. Cette certification est un gage de qualité pour l'exercice futur de votre activité de graphothérapeute. Toutes les personnes qui suivent notre formation ont une même base de connaissances : quand elles ne sont pas graphologues elles-mêmes, elles bénéficient d’une formation de sensibilisation à la graphologie. Ce module préliminaire permet de disposer d’un socle de notions solides pour l’observation fine de l’écriture.
<br><br>
En outre, les graphothérapeutes formés par le GGRE utilisent l’échelle d’évaluation ADE, (Approche Dynamique de l’Ecriture) développée sous la direction d’Adeline Eloy. Cet outil récent permet d’effectuer une analyse fine de l’écriture et de définir les différents axes de la rééducation. La formation dispensée par le GGRE aborde également largement la psychologie de l’enfant et de l’adolescent, indispensable à la compréhension et à la prise en compte globale de la personnalité des patients.</span>
<br><br>

<strong>4/ Les débouchés et lieux d’exercice du graphothérapeute</strong><br>
<span class="Style25">
Le graphothérapeute exerce généralement en profession libérale. Il peut intégrer un cabinet privé ou une structure regroupant différents professionnels tels que des orthophonistes ou des psychomotriciens.Outre l’accompagnement de personnes ayant une écriture dysgraphique, il peut être amené à intervenir auprès d’individus victimes d’accidents ou touchés par une pathologie dégénérative.<br><br>
Vous souhaitez en savoir plus sur notre métier ou notre formation ? <a href="contact-graphotherapeute.php">Prenez contact avec le GGRE</a>. Nous serons ravis de pouvoir vous apporter des renseignements complémentaires.</span>
